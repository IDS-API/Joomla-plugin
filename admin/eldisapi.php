<h3>IDS KS Open API Joomla Plugin documentation</h3>

<p><a href="http://southasia.oneworld.net/" >OneWorld South Asia</a> have developed a plug in for the <a href="http://www.joomla.org/">Joomla</a> CMS to showcase <a href="http://api.ids.ac.uk/about/what-is-the-data/" >IDS Knowledge Services content</a> of thematically organised and hand selected academic research on poverty reduction in the developing world that is freely available to access online.</p>
<p>The plug in allows the administrator to select content from either <a href="http://www.eldis.org">Eldis</a> or <a href="http://www.bridge.ids.ac.uk" >BRIDGE</a>, and import each document as new articles into the site. Once set up, this plugin will automatically bring in new content to your site whenever it is added to the Eldis or BRIDGE websites</p>
<p>You can select:</p>
<ul>
<li>Whether you want Eldis or BRIDGE documents</li>
<li>The number of documents to import</li>
<li>The date range of when the documents were imported</li>
<li>The year of publication</li>
<li>The name of the author</li>
<li>The name of the publisher</li>
<li>The country/ies that the document focuses on</li>
<li>Any keywords assigned to the document</li>
<li>Any number of themes that are associated with the document</li>
</ul>